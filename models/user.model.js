import mongoose from "mongoose";

const userSchema = new mongoose.model({
    name:{
        type:String,
        required:true
    },
    username:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    }
})

const userModel = new mongoose.model("user",userSchema);

export default userModel